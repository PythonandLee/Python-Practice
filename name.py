# .title() is a command to change uppercase for the first character
# .upper() is a command to change every characters into uppercase
# .lower() is a command to change every characters into lowercase

name = 'maRtin luTHer KING'
print(name.title())
print(name.upper())
print(name.lower())

first_name = 'martin'
last_name = 'king'
full_name = first_name + ' '+ last_name
message = 'Hello, ' + full_name.title() + '!'
print(message)